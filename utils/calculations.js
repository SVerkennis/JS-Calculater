export function add() {
        const firstNumber = Number(prompt("Enter first number, please!"));
        const secondNumber = Number(prompt("Enter second number, please!"));
        alert(firstNumber + secondNumber);
}

export function subtract() {
        const firstNumber = Number(prompt("Enter first number, please!"));
        const secondNumber = Number(prompt("Enter second number, please!"));
        alert(firstNumber - secondNumber);
}

export function multiply() {
        const firstNumber = Number(prompt("Enter first number, please!"));
        const secondNumber = Number(prompt("Enter second number, please!"));
        alert(firstNumber * secondNumber);
}

export function divide() {
        const firstNumber = Number(prompt("Enter first number, please!"));
        const secondNumber = Number(prompt("Enter second number, please!"));
        alert(firstNumber / secondNumber);
}



